    private void jcbExamidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbExamidActionPerformed
        // TODO add your handling code here:
        if(jcbExamid.getSelectedIndex()==0)
        {
            return;
        }
        if(jcbExamid.getItemCount()!=0)
        {
            try{
                PerformencePojo pojo=   PERFORMENCEDAO.getlangperByExamid(jcbExamid.getSelectedItem().toString());
                txtlanguage.setText(pojo.getLanguage());
                txtpercentage.setText(String.valueOf(pojo.getPer()));
            
            }catch(SQLException e)
            {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null,"Error in DB","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jcbExamidActionPerformed

