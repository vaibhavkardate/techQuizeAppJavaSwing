    private void loadExamId() {
        try{
            ArrayList<String> examid= PERFORMENCEDAO.getAllExamId(userid);
           jcbExamid.removeAllItems();
           jcbExamid.addItem("");
            for(String id:examid)
            {
                jcbExamid.addItem(id);
            }
    
        }catch(SQLException ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"error in DB","Error",JOptionPane.ERROR_MESSAGE);
        }
        }
